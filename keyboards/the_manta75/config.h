#pragma once

#include "config_common.h"

/* USB Device descriptor parameter */
#define VENDOR_ID       0x21C0
#define PRODUCT_ID      0x0775
#define DEVICE_VER      0x0001
#define MANUFACTURER    Rico
#define PRODUCT         The Manta75

/* key matrix size */
#define MATRIX_ROWS 6
#define MATRIX_COLS 16

/*
 * Keyboard Matrix Assignments
 *
 * Change this to how you wired your keyboard
 * COLS: AVR pins used for columns, left to right
 * ROWS: AVR pins used for rows, top to bottom
 * DIODE_DIRECTION: COL2ROW = COL = Anode (+), ROW = Cathode (-, marked on diode)
 *                  ROW2COL = ROW = Anode (+), COL = Cathode (-, marked on diode)
 *
*/
#define MATRIX_ROW_PINS { D0, C7, D5, D3, D2, D1 }
#define MATRIX_COL_PINS { C6, B6, B5, B4, F7, F6, F5, F4, F1, B7, B3, B2, B1, B0, F0, D7 }
#define UNUSED_PINS { D4, D6, E6 }

/* COL2ROW or ROW2COL */
#define DIODE_DIRECTION COL2ROW

/* Set 0 if debouncing isn't needed */
#define DEBOUNCING_DELAY 5


